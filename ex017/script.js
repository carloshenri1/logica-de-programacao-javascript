var primeiroValor = Number(prompt ("Digite número 1:"))
var segundoValor = Number(prompt ("Digite número: 2"))
var terceiroValor = Number(prompt ("Digite número: 3"))
var quartoValor = Number(prompt ("Digite número: 4"))
